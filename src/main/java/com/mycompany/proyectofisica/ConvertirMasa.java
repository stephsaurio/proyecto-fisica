   private void convertir() {
        try {
            double valor = Double.parseDouble(entrada.getText());
            String unidadFrom = (String) jComboBox1.getSelectedItem();
            String unidadTo = (String) jComboBox2.getSelectedItem();

            double resultado = realizarConversion(valor, unidadFrom, unidadTo);
            Salida.setText(String.format("%.2f", resultado));
        } catch (NumberFormatException e) {
            Salida.setText("Error: entrada no v치lida");
        }
    }

    private double realizarConversion(double valor, String from, String to) {
        // Conversi칩n a kilogramos como base
    if (from.equals("kilogramo")) valor *= 1; // Base: kilogramos
    else if (from.equals("gramo")) valor /= 1000; // 1 kilogramo = 1000 gramos
    else if (from.equals("libra")) valor *= 0.453592; // 1 libra = 0.453592 kilogramos
    else if (from.equals("onza")) valor *= 0.0283495; // 1 onza = 0.0283495 kilogramos

    // Convertir de kilogramos a la unidad deseada
    if (to.equals("kilogramo")) return valor; // Ya est치 en kilogramos
    else if (to.equals("gramo")) return valor * 1000; // Convertir a gramos
    else if (to.equals("libra")) return valor / 0.453592; // Convertir a libras
    else if (to.equals("onza")) return valor / 0.0283495; // Convertir a onzas

        return valor; // Si es la misma unidad, solo retorna el valor
    
    }

